'''
En una lista encontraremos diferentes operaciones 
relacionales, calcular mentalmente el resultado de cada 
expresión y almacenarlo en una nueva lista que contendrá 
únicamente valores lógicos True y False.
'''

expresiones = [
    False == True,
    10 >= 2*4,
    33/3 == 11,
    True > False,
    True*5 == 2.5*2
]

print("Resultado de False == True =>", expresiones[0])
print("Resultado de 10 >= 2*4 =>", expresiones[1])
print("Resultado de 33/3 == 11 =>", expresiones[2])
print("Resultado de True > False =>", expresiones[3])
print("Resultado de True*5 == 2.5*2 =>", expresiones[4])