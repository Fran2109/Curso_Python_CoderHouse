'''
Escribir la letra de la canción Me gusta de Manu Chao, utilizando la sentencia de iteración for: 
'''

letra = ["Me gustan los aviones, me gustas tú", "Me gusta viajar, me gustas tú", "Me gusta la mañana, me gustas tú", "Me gusta el viento, me gustas tú", "Me gusta soñar, me gustas tú", "Me gusta la mar, me gustas tú"]

print("Inicio de la letra")
